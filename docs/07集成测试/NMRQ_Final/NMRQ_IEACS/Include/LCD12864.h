#ifndef __LCD12864_H__
#define __LCD12864_H__

//<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
#define LCD12864_WIDTH 128  //宽度
#define LCD12864_HIGH  64   //高度
#define LCD12864_PAGE  8    //页数

//<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
typedef struct cursor
{
	INT8U ucXPos;
	INT8U ucPage;
}CURSOR;					//光标结构体

//<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
typedef struct LCD
{
	INT8U *pucaBuffer;
	CURSOR stCursor;		//液晶结构体
}LCD;
								  	
extern LCD st_LCD12864;		//12864结构


//****************************************************************************
//* 名称：vLCDInit2
//* 功能：初始化
//****************************************************************************
void vLCDInit2(void);

//****************************************************************************
//* 名称：vCleanAll2
//* 功能：清屏  
//****************************************************************************
void vCleanAll2(void);

//****************************************************************************
//* 名称：ucReadData2
//* 功能：读数据
//****************************************************************************
INT8U ucReadData2(void);

//****************************************************************************
//* 名称：vWriteData2
//* 功能：写数据
//****************************************************************************
void vWriteData2(INT8U ucData);

//****************************************************************************
//* 名称：stGetCur
//* 功能：获取光标位置
//****************************************************************************
CURSOR stGetCur(void);

//****************************************************************************
//* 名称：vSetPos
//* 功能：设置光标位置
//****************************************************************************
void vSetCur(INT8U ucPage,INT8U ucXPos);

//****************************************************************************
//* 名称：vPutCusRow
//* 功能：在8×128的格子里显示自定义长度的一行
//****************************************************************************
void vPutCusRow(INT8U ucPage,INT8U ucXPos,INT8U ucWidth,const INT8U *ucaRow) ;
 
//****************************************************************************
//* 名称：vGetCusRow
//* 功能：读取自定义长度的一行
//****************************************************************************
void vGetCusRow(INT8U ucPage,INT8U ucXPos,INT8U ucWidth,INT8U *ucaRow);


#endif

